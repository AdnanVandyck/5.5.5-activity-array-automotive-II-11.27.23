class Vehicle {
    constructor(make, model, year, color, mileage){
        this.make = make;
        this.model;
        this.year;
        this.color;
        this.passenger = 0;
        this.speed = 0;
        this.mileage = mileage;
        this.started = false;
        this.numberOfWheels = 0;
        this.fuel = 100
    }
}